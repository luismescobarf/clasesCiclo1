#Requerimiento: Programa que recibe dos números enteros y reporta
#si la suma de ellos es positiva.

#Objetivo -> Reportar solamente si es positiva la suma
#de los números ingresados

#Agloritmo (pseudocódigo)
# 1) Cargar desde el teclado los valores de los números
# 2) Sumamos y guardamos el resultado
# 3) Si el resultado de la suma es positivo
#     3a) Reportamos

#Traducción
a = int(input('Ingrese el primer valor: '))
b = int(input('Ingrese el segundo valor: '))
c = a + b
if c > 0:
    print("La sumatoria es positiva")




